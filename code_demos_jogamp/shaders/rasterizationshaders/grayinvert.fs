 ** Simple shader wich converts the primary color to grayscale using NTSC conversion weights and
 ** inverts the calculated gray value. 
 **
 **/

void main(void) {
    float gray = dot(gl_Color.rgb, vec3(0.299, 0.587, 0.114));
    gray = 1.0 - gray;
    gl_FragColor = vec4(gray, gray, gray, 1.0);
}
